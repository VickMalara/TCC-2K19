            <h1>CRIANDO SITES (PROJETOS)</h1>
            <p>O botão <span>CRIAR</span> serve para que você crie páginas para internet sem precisar saber programar.</p>
            <p>Quando você clicar nele, aparecerá na sua tela dois botões, como mostra a imagem:</p>
            <div class="img-div">
                <img src="_learn/_img/site1.png" class="img-fluid" alt="">
            </div>
            <p>O botão <span>COMEÇAR NOVO SITE</span> te leva para outra página para começar a criar seus sites!</p>
            <p>O botão <span>CARREGAR UM SITE</span> mostra todos os sites que você já criou. Se não criou nenhum, irá aparecer só a barra de pesquisa de sites. Para voltar, basta clicar no botão <span>CRIAR</span> de novo.</p>
            <p>
            <p>Na hora de criar um site novo, dá pra você escolher criá-lo no modo livre ou com exercício, escolhendo uma das opções clicando em um dos botões.O salvamento do seu site é automático, então não se preocupe com isso.</p>
            <p>Depois de escolher uma das opções, aparecerá uma nova tela, com um ou dois botões na esquerda e uma página em branco na direita: </p>
            <p><span>Opção no <b>MODO LIVRE</b>:</span></p>
            <div class="img-div">
                <img src="_learn/_img/site2.png" class="img-fluid" alt="">
            </div>
            <p><span>Opção <b>CRIAR COM EXERCÍCIO</b>:</span></p>
            <div class="img-div">
                <img src="_learn/_img/site3.png" class="img-fluid" alt="">
            </div>
            <p>O <span>botão com a paleta de pintor</span> é usado para criar os blocos do seu site, como cabeçalho, blocos de texto e rodapé.</p>
            <p>O <span>botão com a prova</span> mostra o que deve ser feito para concluir o exercício.</p>
            <p>Depois de salvo, dá para baixar o seu site em HTML!Basta você clicar em <span>CRIAR</span>, depois <span>CARREGAR SITE</span> e aparecerá todos os seus sites. Embaixo de cada cartão dos seus sites está o botão <span>Baixar esse site</span>.É só clicar e pronto,já baixou!</p>

